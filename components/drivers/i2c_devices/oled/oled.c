void oled_clear(void) {
  for (int x = 0; x < 8; x++) {
    esp32_i2c_write_byte(OLED_ADDR, 0x00, 0xb0 + x);
    esp32_i2c_write_byte(OLED_ADDR, 0x00, 0x00);
    esp32_i2c_write_byte(OLED_ADDR, 0x00, 0x10);
    for (int y = 0; y < 128; y++) esp32_i2c_write_byte(OLED_ADDR, 0x40, 0x00);
  }
}

void oled_setxy(uint8_t x, uint8_t y) {
  esp32_i2c_write_byte(OLED_ADDR, 0x00, 0xb0 + y);
  esp32_i2c_write_byte(OLED_ADDR, 0x00, (x & 0x0f));
  esp32_i2c_write_byte(OLED_ADDR, 0x00, ((x & 0xf0) >> 4) | 0x10);
}

//初始化oled
bool oled_init() {
  uint8_t cmd_data[] = {0xae, 0x20, 0x10, 0xb0, 0xc8, 0x00, 0x10,
                        0x40, 0x81, 0x7f, 0xa1, 0xa6, 0xa8, 0x3f,
                        0xa4, 0xd3, 0x00, 0xd5, 0xf0, 0xd9, 0x22,
                        0xda, 0x12, 0xdb, 0x20, 0x8d, 0x14, 0xaf};
  esp32_i2c_write_bytes(OLED_ADDR, 0x00, 28, cmd_data);
  oled_clear();
  oled_ascii8(0, 0, "        FISHBOT     ");
  oled_ascii8(0, 1, "Ver :V1.0.0.220820  ");
  // oled_show_ascii_auto_line("2022-07-07");
  ESP_LOGI(FISHBOT_MODLUE, "init success!");
  return true;
}

bool oled_show_ascii_auto_line(char *str) {
  if (strlen(str) > 21) {
    ESP_LOGW(FISHBOT_MODLUE, "len can't >21 char!");
    return false;
  }
  oled_ascii8(0, last_line_, "                       ");
  oled_ascii8(0, last_line_, str);
  last_line_++;
  if (last_line_ > OLED_MAX_LINES) {
    last_line_ = OLED_START_LINE;
  }
  return true;
}

void oled_ascii(uint8_t x, uint8_t y, char *str) {
  oled_setxy(x, y);
  for (int i = 0; i < 16 && (str[i] != 0); i++) {
    for (int j = 0; j < 8; j++) {
      esp32_i2c_write_byte(OLED_ADDR, 0x40, ascii[str[i] - 32][j]);
    }
  }
  oled_setxy(x, y + 1);
  for (int i = 0; i < 16 && (str[i] != 0); i++) {
    for (int j = 8; j < 16; j++) {
      esp32_i2c_write_byte(OLED_ADDR, 0x40, ascii[str[i] - 32][j]);
    }
  }
}

void oled_ascii8(uint8_t x, uint8_t y, char *str) {
  oled_setxy(x, y);
  for (int i = 0; i < 21 && (str[i] != 0); i++) {
    for (int j = 0; j < 6; j++) {
      esp32_i2c_write_byte(OLED_ADDR, 0x40, ascii[str[i] - 32][j]);
    }
  }
}
